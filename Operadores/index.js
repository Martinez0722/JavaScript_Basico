const num1 = 5;
const num2 = 10;
// Adição

console.log(num1+num2);

// Subtração
console.log(num1-num2);

//Divisão
console.log(num1/num2);

//Multiplicação
console.log(num1*num2);

//Resto da divisão
console.log(num1%num2);

//Potenciação
console.log(num1**num2);

// Incremento
let contador = 10;
console.log(++contador)

// Decremento
let decremento = 5
console.log(--decremento)

// Atribuição
